def _is_valid_coord(world, y, x):
    if x < 0 or y < 0 or y >= len(world) or x >= len(world[y]):
        return False
    else:
        return True

def _get_neighbours(world, y, x):
    # 000
    # 0X0
    # 000
    neighbours = []

    # row before
    if _is_valid_coord(world, y - 1, x - 1): # upper left neighbour
        neighbours.append(world[y - 1][x - 1])
    if _is_valid_coord(world, y - 1, x): # upper neighbour
        neighbours.append(world[y - 1][x])
    if _is_valid_coord(world, y - 1, x + 1): # upper right neighbour
        neighbours.append(world[y - 1][x + 1])

    # same row
    if _is_valid_coord(world, y, x - 1): # left neighbour
        neighbours.append(world[y][x - 1])
    if _is_valid_coord(world, y, x + 1): # right neighbour
        neighbours.append(world[y][x + 1])

    # row after
    if _is_valid_coord(world, y + 1, x - 1): # lower left neighbour
        neighbours.append(world[y + 1][x - 1])
    if _is_valid_coord(world, y + 1, x): # lower neighbour
        neighbours.append(world[y + 1][x])
    if _is_valid_coord(world, y + 1, x + 1): # lower right neighbour
        neighbours.append(world[y + 1][x + 1])

    return neighbours

def _get_alive_neighbour_count(world, y, x):
    neighbours = _get_neighbours(world, y, x)
    return neighbours.count(1)

def update(world):
    new_world = []

    for y in range(len(world)):
        new_row = []
        for x in range(len(world[y])):
            num_alive_neighbours = _get_alive_neighbour_count(world, y, x)

            # rule 1 (underpopulation) - alive cell with < 2 alive neighbours -> dead
            if world[y][x] == 1 and num_alive_neighbours < 2:
                new_row.append(0)

            # rule 2 (survival) - alive cell with exactly 2 or 3 alive neighbours -> alive
            elif world[y][x] == 1 and num_alive_neighbours <= 3 and num_alive_neighbours >= 2:
                new_row.append(1)

            # rule 3 (overpopulation) - alive cell with > 3 alive neighbours -> dead
            elif world[y][x] == 1 and num_alive_neighbours > 3:
                new_row.append(0)

            # rule 4 (reproduction) - dead cell with == 3 alive neighbours -> alive
            elif world[y][x] == 0 and num_alive_neighbours == 3:
                new_row.append(1)

            else:
                new_row.append(0)
        new_world.append(new_row)

    return new_world

def draw(stdscr, world):
    stdscr.clear()

    for y in range(len(world)):
        for x in range(len(world[y])):
            if world[y][x] == 1:
                stdscr.addstr(y, x, ' ', curses.color_pair(1))
    
    stdscr.refresh()

