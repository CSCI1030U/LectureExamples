def _get_neighbours(world, y, x):
    neighbours = []

    # 000
    # 0X0
    # 000

    # get the neighbours from the row above
    if _is_valid_coord(world, y - 1, x - 1):
        neighbours.append(world[y - 1][x - 1])
    if _is_valid_coord(world, y - 1, x):
        neighbours.append(world[y - 1][x])
    if _is_valid_coord(world, y - 1, x + 1):
        neighbours.append(world[y - 1][x + 1])

    # get the neighbours from the same row
    if _is_valid_coord(world, y, x - 1):
        neighbours.append(world[y][x - 1])
    if _is_valid_coord(world, y, x + 1):
        neighbours.append(world[y][x + 1])
    
    # get the neighbours from the row below
    if _is_valid_coord(world, y + 1, x - 1):
        neighbours.append(world[y + 1][x - 1])
    if _is_valid_coord(world, y + 1, x):
        neighbours.append(world[y + 1][x])
    if _is_valid_coord(world, y + 1, x + 1):
        neighbours.append(world[y + 1][x + 1])

    return neighbours

def _num_alive_neighbours(world, y, x):
    neighbours = _get_neighbours(world, y, x)
    return neighbours.count(1)

def update(world):
    new_world = []
    for y in range(len(world)):
        new_row = []
        for x in range(len(world[y])):
            num_neighbours = _num_alive_neighbours(world, y, x)

            # rule 1 - alive cell, with alive neighbours < 2 -> dead (underpopulation)
            if world[y][x] == 1 and num_neighbours < 2:
                new_row.append(0)

            # rule 2 - alive cell, with alive neighbours 2 or 3 -> alive (survival)
            elif world[y][x] == 1 and num_neighbours >= 2 and num_neighbours <= 3:
                new_row.append(1)

            # rule 3 - alive cell, with alive neighbours is > 3 -> dead (overpopulation)
            elif world[y][x] == 1 and num_neighbours > 3:
                new_row.append(0)

            # rule 4 - dead cell, with alive neighbours == 3 -> alive (reproduction)
            elif world[y][x] == 0 and num_neighbours == 3:
                new_row.append(1)

            else:
                new_row.append(world[y][x])

        new_world.append(new_row)

    return new_world

