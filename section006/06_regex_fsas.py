def _get_neighbours(world, y, x):
    neighbours = []

    # 000
    # 0X0
    # 000

    # get the neighbours from the row above
    if _is_valid_coord(world, y - 1, x - 1):
        neighbours.append(world[y - 1][x - 1])
    if _is_valid_coord(world, y - 1, x):
        neighbours.append(world[y - 1][x])
    if _is_valid_coord(world, y - 1, x + 1):
        neighbours.append(world[y - 1][x + 1])

    # get the neighbours from the same row
    if _is_valid_coord(world, y, x - 1):
        neighbours.append(world[y][x - 1])
    if _is_valid_coord(world, y, x + 1):
        neighbours.append(world[y][x + 1])

    # get the neighbours from the row below
    if _is_valid_coord(world, y + 1, x - 1):
        neighbours.append(world[y + 1][x - 1])
    if _is_valid_coord(world, y + 1, x):
        neighbours.append(world[y + 1][x])
    if _is_valid_coord(world, y + 1, x + 1):
        neighbours.append(world[y + 1][x + 1])

    return neighbours

def _num_alive_neighbours(world, y, x):
    neighbours = _get_neighbours(world, y, x)
    return neighbours.count(1)

